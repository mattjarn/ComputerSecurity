#define getSBox(num) (sbox[(num)])
#define getInverseSBox(num) (inverse_sbox[(num)])

#define Multiply(x, y)                       \
	(((y & 1) * x) ^                           \
	 ((y >> 1 & 1) * xtime(x)) ^               \
	 ((y >> 2 & 1) * xtime(xtime(x))) ^        \
	 ((y >> 3 & 1) * xtime(xtime(xtime(x)))) ^ \
	 ((y >> 4 & 1) * xtime(xtime(xtime(xtime(x))))))

static uint8_t xtime(uint8_t x)
{
	return ((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

static void keyExpansion(uint8_t *Expandedkey, const uint8_t *key)
{
	uint8_t a[4]; // temporary matrix for operations

	for (uint8_t i = 0; i < ctx.Nk; i++)
	{
		Expandedkey[(i * 4) + 0] = key[(i * 4) + 0];
		Expandedkey[(i * 4) + 1] = key[(i * 4) + 1];
		Expandedkey[(i * 4) + 2] = key[(i * 4) + 2];
		Expandedkey[(i * 4) + 3] = key[(i * 4) + 3];
	}

	for (uint8_t i = ctx.Nk; i < Nb * (ctx.Nr + 1); i++)
	{
		{
			uint8_t k = (i - 1) * 4;
			a[0] = Expandedkey[k + 0];
			a[1] = Expandedkey[k + 1];
			a[2] = Expandedkey[k + 2];
			a[3] = Expandedkey[k + 3];
		}

		if (i % ctx.Nk == 0)
		{
			// [a0,a1,a2,a3] becomes [a1,a2,a3,a0]
			{
				const uint8_t u8temp1 = a[0];
				a[0] = a[1];
				a[1] = a[2];
				a[2] = a[3];
				a[3] = u8temp1;
			}

			// [a0,a1,a2,a3] becomes [sbox[a0],sbox[a1],sbox[a2],sbox[a3]]
			{
				a[0] = getSBox(a[0]);
				a[1] = getSBox(a[1]);
				a[2] = getSBox(a[2]);
				a[3] = getSBox(a[3]);
			}

			a[0] = a[0] ^ PowersOf2[i / ctx.Nk];
		}

		if (ctx.Nk == 8)
		{
			if (i % ctx.Nk == 4)
			{
				{
					a[0] = getSBox(a[0]);
					a[1] = getSBox(a[1]);
					a[2] = getSBox(a[2]);
					a[3] = getSBox(a[3]);
				}
			}
		}

		uint8_t j = i * 4;
		uint8_t k = (i - ctx.Nk) * 4;
		Expandedkey[j + 0] = Expandedkey[k + 0] ^ a[0];
		Expandedkey[j + 1] = Expandedkey[k + 1] ^ a[1];
		Expandedkey[j + 2] = Expandedkey[k + 2] ^ a[2];
		Expandedkey[j + 3] = Expandedkey[k + 3] ^ a[3];
	}
}

static void AddKeyToState(uint8_t round, state_t *state, const uint8_t *Roundkey)
{
	for (uint8_t i = 0; i < 4; ++i)
	{
		for (uint8_t j = 0; j < 4; ++j)
		{
			(*state)[i][j] ^= Roundkey[(round * Nb * 4) + (i * Nb) + j];
		}
	}
}

static void SubBytes(state_t *state, int inverseFlag)
{
	for (uint8_t i = 0; i < 4; i++)
	{
		for (uint8_t j = 0; j < 4; j++)
		{
			if (!inverseFlag)
				(*state)[j][i] = getSBox((*state)[j][i]);
			else
				(*state)[j][i] = getInverseSBox((*state)[j][i]);
		}
	}
}

static void ShiftRows(state_t *state, int inverseFlag)
{
	if (!inverseFlag)
	{
		// Shift first row
		uint8_t temp = (*state)[0][1];
		(*state)[0][1] = (*state)[1][1];
		(*state)[1][1] = (*state)[2][1];
		(*state)[2][1] = (*state)[3][1];
		(*state)[3][1] = temp;

		// Shift second row
		temp = (*state)[0][2];
		(*state)[0][2] = (*state)[2][2];
		(*state)[2][2] = temp;
		temp = (*state)[1][2];
		(*state)[1][2] = (*state)[3][2];
		(*state)[3][2] = temp;

		// Shift third row
		temp = (*state)[0][3];
		(*state)[0][3] = (*state)[3][3];
		(*state)[3][3] = (*state)[2][3];
		(*state)[2][3] = (*state)[1][3];
		(*state)[1][3] = temp;
	}
	else
	{
		// Shift the first row
		uint8_t temp = (*state)[3][1];
		(*state)[3][1] = (*state)[2][1];
		(*state)[2][1] = (*state)[1][1];
		(*state)[1][1] = (*state)[0][1];
		(*state)[0][1] = temp;

		// Shift the second row
		temp = (*state)[0][2];
		(*state)[0][2] = (*state)[2][2];
		(*state)[2][2] = temp;
		temp = (*state)[1][2];
		(*state)[1][2] = (*state)[3][2];
		(*state)[3][2] = temp;

		// shift the third row
		temp = (*state)[0][3];
		(*state)[0][3] = (*state)[1][3];
		(*state)[1][3] = (*state)[2][3];
		(*state)[2][3] = (*state)[3][3];
		(*state)[3][3] = temp;
	}
}

static void MixColumns(state_t *state, int inverseFlag)
{
	if (!inverseFlag)
	{
		for (uint8_t i = 0; i < 4; i++)
		{
			uint8_t i0 = (*state)[i][0];
			uint8_t temp1 = (*state)[i][0] ^ (*state)[i][1] ^ (*state)[i][2] ^ (*state)[i][3];
			uint8_t temp2 = (*state)[i][0] ^ (*state)[i][1];
			temp2 = xtime(temp2);
			(*state)[i][0] ^= (temp2 ^ temp1);
			temp2 = (*state)[i][1] ^ (*state)[i][2];
			temp2 = xtime(temp2);
			(*state)[i][1] ^= (temp2 ^ temp1);
			temp2 = (*state)[i][2] ^ (*state)[i][3];
			temp2 = xtime(temp2);
			(*state)[i][2] ^= (temp2 ^ temp1);
			temp2 = (*state)[i][3] ^ i0;
			temp2 = xtime(temp2);
			(*state)[i][3] ^= (temp2 ^ temp1);
		}
	}
	else
	{
		for (uint8_t i = 0; i < 4; i++)
		{
			uint8_t a = (*state)[i][0];
			uint8_t b = (*state)[i][1];
			uint8_t c = (*state)[i][2];
			uint8_t d = (*state)[i][3];

			(*state)[i][0] = Multiply(a, 0x0e) ^ Multiply(b, 0x0b) ^ Multiply(c, 0x0d) ^ Multiply(d, 0x09);
			(*state)[i][1] = Multiply(a, 0x09) ^ Multiply(b, 0x0e) ^ Multiply(c, 0x0b) ^ Multiply(d, 0x0d);
			(*state)[i][2] = Multiply(a, 0x0d) ^ Multiply(b, 0x09) ^ Multiply(c, 0x0e) ^ Multiply(d, 0x0b);
			(*state)[i][3] = Multiply(a, 0x0b) ^ Multiply(b, 0x0d) ^ Multiply(c, 0x09) ^ Multiply(d, 0x0e);
		}
	}
}

int init(const uint8_t *key, const size_t len)
{
	if (len == 32)
	{
		ctx.Nk = 8;	//sets the Nk
		ctx.Nr = 14; // set the Nr
	}
	else if (len == 24)
	{
		ctx.Nk = 6;	//sets the Nk
		ctx.Nr = 12; // sets the Nr
	}
	else
	{
		ctx.Nk = 4;	//set the Nk
		ctx.Nr = 10; //set the Nr
	}

	keyExpansion(ctx.schedule, key); //- Initialize the schedule via key expansion

	return 1; // Indicates a successful initialization
}

void encrypt(const uint8_t *in, uint8_t *out)
{
	AddKeyToState(0, (state_t *)in, ctx.schedule);

	// Perform Nr-1 Rounds
	for (uint8_t round = 1; round < ctx.Nr; round++)
	{
		SubBytes((state_t *)in, 0);
		ShiftRows((state_t *)in, 0);
		MixColumns((state_t *)in, 0);
		AddKeyToState(round, (state_t *)in, ctx.schedule);
	}

	// Perform The Last Round
	SubBytes((state_t *)in, 0);
	ShiftRows((state_t *)in, 0);
	AddKeyToState(ctx.Nr, (state_t *)in, ctx.schedule);
	for (int i = 0; i <= 15; i++)
	{
		out[i] = in[i];
	}
}

void decrypt(const uint8_t *in, uint8_t *out)
{
	AddKeyToState(ctx.Nr, (state_t *)in, ctx.schedule);

	for (uint8_t round = (ctx.Nr - 1); round > 0; round--)
	{
		ShiftRows((state_t *)in, 1);
		SubBytes((state_t *)in, 1);
		AddKeyToState(round, (state_t *)in, ctx.schedule);
		MixColumns((state_t *)in, 1);
	}

	ShiftRows((state_t *)in, 1);
	SubBytes((state_t *)in, 1);
	AddKeyToState(0, (state_t *)in, ctx.schedule);
	for (int i = 0; i <= 15; i++)
	{
		out[i] = in[i];
	}
}


